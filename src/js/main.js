document.addEventListener("DOMContentLoaded", () => {
    const slides = document.querySelectorAll(".swiper1 .swiper-slide");
    const catalogs = document.querySelectorAll(".catalog__open");

    // Производители
    catalogs.forEach((item, index) => {
        item.style.display = index === 0 ? "block" : "none";
    });

    slides.forEach((slide, index) => {
        slide.addEventListener("click", () => {
            slides.forEach(item => item.classList.remove("active"));
            catalogs.forEach(item => item.style.display = "none");

            slide.classList.add("active");

            if (catalogs[index]) {
                catalogs[index].style.display = "block";
            }
        });
    });

    // Табы моделей
    document.querySelectorAll(".tabs").forEach(tab => {
        const buttons = tab.querySelectorAll(".tabs__btns > div");
        const areas = tab.querySelectorAll(".tabs__area");

        // Инициализация
        areas.forEach((area, index) => {
            area.style.display = index === 0 ? "flex" : "none";
        });

        buttons.forEach((btn, index) => {
            btn.addEventListener("click", () => {
                buttons.forEach(item => item.classList.remove("active"));
                btn.classList.add("active");

                areas.forEach(area => {
                    area.style.display = "none";
                });

                if (areas[index]) {
                    areas[index].style.display = "flex";
                }
            });
        });
    });
});
